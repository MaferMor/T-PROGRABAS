frase = input("Ingrese una frase: ")

mayusculas = frase.upper()

minusculas = frase.lower()

EraMayuscula = frase.title()

print("Frase en mayúsculas:")
print(mayusculas)
print("\nFrase en minúsculas:")
print(minusculas)
print("\nFrase con la primera letra de cada palabra en mayúscula:")
print(EraMayuscula)
